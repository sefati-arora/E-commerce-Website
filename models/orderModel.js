module.exports=(Sequelize,sequelize,DataTypes) =>
{
    return sequelize.define(
        "orderTable",
        {
            ...require('./core')(Sequelize,DataTypes),
            userId:
            {
                type:Sequelize.UUID,
                allowNull:true,
                references:
                {
                    model:"userTable",
                    key:"id"
                },
                onUpdate:"CASCADE",
                onDelete:"CASCADE"
            },
            productId:
            {
                type:Sequelize.UUID,
                allowNull:true,
                references:
                {
                    model:"productTable",
                    key:"id"
                },
                onUpdate:"CASCADE",
                onDelete:"CASCADE"
            },
            addressId:
            {
              type:Sequelize.UUID,
              allowNull:true,
              references:
              {
                model:"addressTable",
                key:"id"
              },
              onUpdate:"CASCADE",
              onDelete:"CASCADE"
            },
            Amount:
            {
                type:DataTypes.DOUBLE,
                allowNull:true
            }
        },
        {
            tableName:"orderTable"
        }
    )
}